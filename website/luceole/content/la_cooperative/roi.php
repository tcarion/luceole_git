              </p>
              <p style="font-size:15px;">
                <b>Chapitre 1 - OBJECTIFS</b>
              </p>
              <p>
                <b>Article 1- PRECISION DE CERTAINS OBJECTIFS DE LA COOPERATIVE</b>
              </p>
              <p>
                Dans l’article 3 de ses statuts (Objet social de Lucéole), la société explicite ses objectifs coopératifs. Plus
                précisément, Lucéole veut, par ses actions et dans sa gestion, respecter les principes de la citoyenneté
                participative et du Développement Durable tels que définis dans <a href="" title="Définition proposée en 1987 par la
                Commission mondiale sur l’environnement et le développement dans le rapport Brundtland,
                le développement durable est : « un développement qui répond aux besoins des générations du présent sans compromettre la
                capacité des générations futures à répondre aux leurs. Deux concepts sont inhérents à cette notion : le concept de « besoins », et
                plus particulièrement des besoins essentiels des plus démunis à qui il convient d’accorder la plus grande priorité, et l’idée des
                limitations que l’état de nos techniques et de notre organisation sociale impose sur la capacité de l’environnement à répondre
                aux besoins actuels et à venir.">le rapport Brundtland</a>. Lucéole SCRL
                vise aussi les objectifs suivants :
              </p>
              <ul>
              <li>
                Faire bénéficier ses coopérateurs de services liés à l’achat d’énergie à tarifs préférentiels par
                exemple en leur permettant d’accéder à l’électricité produite par les installations de la
                coopérative,
              </li>
              <li>
                Pousser les coopérateurs à l’Utilisation Rationnelle de l’Énergie (URE) par des informations,
                primes, aides et incitants : « L’énergie la plus respectueuse de l’environnement est celle qui n’est
                pas utilisée. »,
              </li>
              <li>
                Inciter les coopérateurs à utiliser des énergies renouvelables ayant un faible impact
                environnemental,
              </li>
              <li>
                Aider au développement de la citoyenneté responsable et active, à la préservation de
                l’environnement et de la biodiversité aux fins de bénéfices environnementaux et sociétaux,
              </li>
              <li>
                Permettre à tous d’accéder au statut de coopérateur, y compris les citoyens à faible revenu, les
                personnes en situation précaire et les Associations Sans But Lucratif, aux fins de bénéfices
                sociaux,
              </li>
              <li>
                La société veut favoriser le développement économique local par la création d’emploi dans le
                développement durable.
              </li>
              <li>
                Manifester sa volonté de sobriété en ne recherchant qu’un bénéfice patrimonial limité.
              </li>
              </ul>
              <p>
                L’objet social de la coopérative se trouve autrement expliqué ci-après par l’expression d’une vision :
                <i>« Lucéole contribue à un monde plus juste et plus respectueux de chacun et de l’environnement. La
                démarche de Lucéole est fondée sur les principes de l’économie solidaire, de la participation citoyenne et
                intergénérationnelle et de l’ancrage territorial. L’action de la coopérative se base sur la production
                d’énergies renouvelables et sur le développement de l’autonomie et de la sobriété énergétiques ».</i>
              </p>
              <p>
                La coopérative Lucéole a donc une vision sociale et vise prioritairement le développement local. Elle est
                ancrée en Sud Luxembourg. Toute personne physique, quelle que soit sa nationalité, ou toute personne
                morale peut devenir coopérateur à condition de partager les objectifs de la coopérative tels qu’énoncés
                dans les statuts et dans le présent règlement.
              </p>

              <p style="font-size:15px;">
                <b>Chapitre 2 – ADMISSION ET GESTION DES COOPERATEURS</b>
              </p>
              <p>
                <b>Article 2 – CONSIDERATIONS GENERALES</b>
              </p>
              <p>
                C’est le Conseil d’Administration (CA) de la coopérative qui accepte ou refuse les coopérateurs sur base
                de leur demande. Le CA évitera les conflits d’intérêts aussi bien privés que publics, la surcapitalisation qui
                serait néfaste aux services proposés.
              </p>
              <p>
                Le CA veille à la diversité de ses coopérateurs (sexe, situation familiale ou professionnelle, origine, niveau
                d’études…). Le CA s’efforcera aussi de promouvoir la diversité dans ses rangs.
              </p>
              <p>
                Le CA, dans son acceptation de nouveaux coopérateurs, veillera à préserver l’indépendance de la
                coopérative.
              </p>
              <p>
                Pour permettre aux personnes à revenus modestes d’accéder au statut de coopérateur, Lucéole pourra
                contracter avec des associations, CPAS ou services d’aide sociale. Dans la poursuite de ses objectifs, la
                coopérative peut recourir à de l’épargne externe, interne, tiers investisseur public ou privé, etc.…
              </p>
              <p>
                <b>Article 3 – PROCEDURES POUR DEVENIR COOPERATEUR</b>
              </p>
              <p>
                <u>Cas 1 : Personne physique qui devient elle-même coopératrice</u>
              </p>
              <p>
                Le CA reçoit les candidatures via le site internet ou un formulaire papier.
              </p>
              <p>
                Si les données administratives sont complètes, la candidature est soumise au CA pour acceptation dans
                les 7 jours. Le CA a le droit de refuser ou de demander des précisions pour une candidature qui
                semblerait en contradiction avec le ROI ou les statuts.
              </p>
              <p>
                Le candidat est ensuite invité à libérer ses parts sur le compte de Lucéole. Ses données administratives
                sont ensuite transcrites au registre dans le mois qui suit la libération et un certificat lui est envoyé par
                mail ou à défaut par courrier.
              </p>
              <p>
                Le CA privilégie l’adhésion par mail afin de limiter le coût administratif. Toutefois si une personne ne
                dispose pas d’un accès à internet, la voie papier est possible.
              </p>
              <p>
                Note : si le coopérateur a moins de 18 ans il est représenté dans ses démarches par son tuteur légal et
                celui-ci est indiqué au registre.
              </p>
              <p>
                Le candidat devient coopérateur, après acceptation du CA et à la date de libération de sa(ses) part(s) sur
                le compte de Lucéole. Il est officiellement inscrit au registre des coopérateurs dans les 15 jours qui
                suivent.
              </p>
              <p>
                <u>Cas 2 : Personne physique qui est parrainée par une autre personne physique</u>
              </p>
              <p>
                La procédure comporte 2 étapes et se fait par mail uniquement afin de limiter le coût administratif.
              </p>
              <p>
                ETAPE1 Le parrain remplit un formulaire ad hoc via le site internet « offrir des parts de Lucéole » et libère
                les parts cadeau pour son filleul (limitées à 10 maximum); il indique ensuite la date d’activation qui est la
                date à partir de laquelle le CA peut communiquer avec le filleul sans briser « l’effet de surprise ». La
                candidature est soumise au CA et, en cas d’acceptation, le parrain est invité à contacter le filleul pour
                l’étape 2 ci-dessous.
              </p>
              <p>
                ETAPE2 Le filleul (ou son tuteur si moins de 18 ans) remplit à son tour sur le site un formulaire « devenir
                coopérateur » en indiquant en remarques le nom de son parrain et la date d’activation. Le CA disposera
                ainsi de toutes les données (date de naissance, compte en banque, adresse,…) pour l’inscrire au registre
                et lui remettre un certificat.
              </p>
              <p>
                Le filleul devient coopérateur quand les 3 conditions ci-dessous sont remplies :
              </p>
                <ol>
                <li>
                les parts sont libérées ;
                </li>
                <li>
                la date d’activation est passée ;
                </li>
                <li>
                l’étape 2 est finalisée.
                </li>
                </ol>
              <p>
                Il est officiellement inscrit au registre des coopérateurs dans les 15 jours qui suivent.
              </p>
              <p>
                <u>Cas 3 : Personne morale</u>
              </p>
              <p>
                Une « personne morale » désigne une construction juridique à laquelle la loi confère des droits
                semblables à ceux des personnes physiques (nom, domicile, nationalité, droit d’acquérir, d’administrer
                et de céder un patrimoine…). Ainsi, sont des personnes morales, on dit aussi « personnes juridiques »,
                l’Etat, les Provinces, les Communes, les établissements publics, les associations sans but lucratif (ASBL),
                les sociétés commerciales (SA, SPRL, SC…), les fondations…
              </p>
              <p>
                Le CA reçoit les candidatures via le site internet ou un formulaire papier. Le candidat prendra soin
                d’indiquer en remarques les noms, téléphones et adresses mails de 2 de ses représentants légaux. Si la
                personne morale est une Commune, ces mêmes données relatives au Bourgmestre et au Secrétaire
                Communal seront indiquées.
              </p>
              <p>
                Si les données administratives sont complètes, la candidature est soumise au CA pour acceptation dans
                les 7 jours. Le CA a le droit de refuser ou de demander des précisions pour une candidature qui
                semblerait en contradiction avec le ROI ou les statuts.
                La personne morale candidate est ensuite invitée à libérer ses parts sur le compte de Lucéole. Ses
                données administratives sont ensuite transcrites au registre dans le mois qui suit la libération et un
                certificat lui est envoyé par mail.
              </p>
              <p>
                La personne morale devient coopératrice après acceptation du CA et à la date de libération sur le compte
                de Lucéole.
              </p>
              <p>
                Elle est officiellement inscrite au registre des coopérateurs dans les 15 jours qui suivent.
              </p>
              <p>
                <b>Article 4 – TENUE DU REGISTRE DES COOPERATEURS</b>
              </p>
              <p>
                Chaque coopérateur est inscrit au registre des coopérateurs de Lucéole. Il est convenu par le présent
                règlement approuvé en AG ordinaire de mai 2013 que celui-ci est géré en format électronique (Excell).
                Le registre contient au moins :
              </p>
              <ul>
              <li>
                les noms, prénoms et domicile de chaque associé (nom du tuteur si < 18ans)
              </li>
              <li>
                le nombre de parts dont il est titulaire ainsi qu’un explicatif et les dates de mouvement de parts
                éventuels (remboursement, achats de nouvelles parts, réception de parts parrainage…)
              </li>
              <li>
                le montant et la date des versements effectués lors des distributions de dividendes
              </li>
              <li>
                le numéro d’ordre du coopérateur
              </li>
              <li>
                type de coopérateur (personne physique, garant, personne morale)
              </li>
              </ul>
              <p>
                Les informations suivantes sont collectées obligatoirement, mais non partagées hors du CA afin de
                protéger la vie privée des coopérateurs :
              </p>
              <ul>
              <li>
                date, lieu de naissance
              </li>
              <li>
                documents notariaux probants en cas de remboursement pour cause de décès…
              </li>
              <li>
                le compte bancaire au format IBAN /BIC et le nom du titulaire pour le versement des dividendes
              </li>
              <li>
                l’engagement d’adhérer aux statuts et au ROI de la coopérative
              </li>
              </ul>
              <p>
                Les informations suivantes sont facultatives mais également collectées sans être diffusées hors du CA :
              </p>
              <ul>
              <li>
                adresse email et accord pour recevoir la communication par voie électronique (facultatif mais
                vivement recommandé pour limiter le coût administratif).
              </li>
              <li>
                numéro de téléphone et/ou gsm.
              </li>
              <li>
                comment le coopérateur a-t-il connu Lucéole, par quel coopérateur ?
              </li>
              <li>
                compétences disponibles pour Lucéole
              </li>
              <li>
                remarques éventuelles.
              </li>
              </ul>
              <p>
                Le registre des coopérateurs en version électronique peut être consulté uniquement par des coopérateurs
                et au siège social de Lucéole, cela moyennant un rendez-vous préalable avec le président ou le
                responsable administratif du CA. Une copie électronique est consultable au domicile des mêmes
                personnes également sur simple rendez-vous.
              </p>
              <p>
                <b>Article 5 – LES COOPERATEURS GARANTS</b>
              </p>
              <p>
                <b>Article 5.1 Définition </b> (statuts Art.2 et Art.9)
              </p>
              <p>
                Le « Collège des Garants » est composé de l’ensemble des détenteurs de « parts A » telles que définies
                par les statuts.
              </p>
              <p>
                Sa mission est de protéger la coopérative contre toute dérive de gestion, d’investissement et d’action qui
                irait à l’encontre de l’esprit initial du projet.
              </p>
              <p>
                Le Collège des Garants peut élargir ses rangs en choisissant parmi les coopérateurs des personnes
                physiques qui font état d’un engagement fort vis-à-vis des statuts et des objectifs de la coopérative. Le
                Collège des Garants peut s’élargir au maximum à 150% du nombre de coopérateurs Garants initiaux.
              </p>
              <p>
                <b>Article 5.2 procédure de cooptation </b> : demande personnelle ou proposition du collège des garants.
              </p>
              <p>
                L’AG a pour mission de valider l’entrée et la sortie de coopérateurs garants. Ceux-ci sont au nombre de 60
                avant l’AG 2013 et peuvent être maximum 150 % des coopérateurs fondateurs soit 55x 1,5 = 82
                coopérateurs garants (art 9 & 2 statuts)
              </p>
              <p>
                La procédure d’élection et de départ des coopérateurs garants :
              </p>
              <p>
                <ul>
                  <li>
                Un mois minimum avant l’AG les candidats à la qualité de coopérateurs garants se signalent par
                courrier électronique ou papier au président de la coopérative avec copie au secrétaire.
              </li>
              <li>
                Lors de l’AG, les candidats coopérateurs garants présentent leurs motivations.
              </li>
              <li>
                Les participants à l’AG posent les questions qu’ils souhaitent en relation avec l’objet de la
                candidature.
              </li>
              <li>
                La candidature des coopérateurs est soumise au vote de l’assemblée des coopérateurs garants de
                la coopérative. Un candidat est élu s’il obtient au moins la moitié des voix plus une des
                coopérateurs garants présents ou représentés lors de l’AG.
              </li>
              <li>
                Les coopérateurs garants sortants se signalent lors de l’AG. S’ils le souhaitent, ils ont l’occasion de
                présenter les raisons de leur retrait.
              </li>
              <li>
                Le PV de l’AG acte l’entrée et le retrait des coopérateurs garants.
              </li>
              </ul>
            </p>
            <p style="font-size:15px;">
              <b>Chapitre 3 – ORGANISATION DE LA SOCIETE</b>
            </p>
            <p>
              <b>Article 6 - Fonctionnement du Conseil d'Administration</b>
            </p>
            <p>
              Le Conseil d’Administration est élu par les coopérateurs de Lucéole SCRL comme précisé dans l’article 15
              des statuts.
            </p>
            <p>
              <b>Article 6.1 Appel à candidatures d’Administrateurs</b>
            </p>
            <p>
              Lorsqu’un poste d’Administrateur est vacant (CA comptant moins de 9 membres), ou lors du
              renouvellement statutaire des Administrateurs, le CA en exercice, lance un appel à candidatures auprès
              des coopérateurs, au moins 30 jours avant la prochaine AG ordinaire ou extraordinaire permettant
              l’élection d’Administrateurs par les coopérateurs.
            </p>
            <p>
              Les candidatures écrites, doivent parvenir (de préférence par courriel, avec accusé de réception) au
              Président du CA avec copie au secrétaire au moins 16 jours avant l’AG et seront jointes aux convocations
              15 jours avant l’AG.
            </p>
            <p>
              Si le nombre de candidats déclarés par écrit est insuffisant pour atteindre le nombre minimum de 7
              administrateurs, des candidatures orales spontanées proposées lors de l’AG seront acceptées.
              Les candidats au CA sont tenus de déclarer par écrit sur l’honneur, au moment du dépôt de leur
              candidature qu’ils n’exercent pas de mandat politique ou tout autre mandat public ou privé, qui serait en
              conflit d’intérêt avec la coopérative. Tout changement de situation personnelle vis-à-vis de ces mandats
              publics ou privés qui interviendrait en cours de mandat d’Administrateur de Lucéole doit immédiatement
              être déclaré au CA qui décide ou non du maintien de l’administrateur jusqu’à la prochaine AG.
            </p>
            <p>
              L'article 18 des statuts précise : « Le Conseil d’Administration choisit parmi ses membres un président. II se
              réunit sur convocation et sous la présidence de son président ou, en cas d’empêchement, de celle de
              l’administrateur le plus âgé, et chaque fois que deux administrateurs au moins le demandent. »
            </p>
            <p>
              Le Conseil d'Administration délibère comme indiqué à l'article 18 des statuts. : « Le Conseil
              d’Administration ne peut délibérer valablement que si la moitié au moins de ses membres sont présents ou
              représentés. Chaque administrateur ne peut être porteur que d’une seule procuration représentant un
              autre administrateur. Les décisions sont prises à la majorité absolue (la moitié des voix plus une) des
              membres présents et représentés. Il n’est pas tenu compte des abstentions. »
            </p>
            <p>
              En cas de parité de voix, le président tranchera.
            </p>
            <p>
              Si au moins un administrateur le demande, le vote se fera à bulletin secret.
              L'élection du président du CA se tient à bulletin secret et requiert une majorité de deux tiers des
              administrateurs présents ou représentés, avec au moins une moitié d'entre eux présents ou représentés.
              Les abstentions ne sont pas prises en compte. Tant que le Conseil d'Administration ne réussit pas à élire
              un président à cette majorité, le poste sera assuré, au choix du Conseil, soit par l'ancien président soit par
              l'administrateur le plus âgé.
            </p>
            <p>
              Le mandat du président est de 4 ans. Il est renouvelable 2 fois, c'est-à-dire trois mandats successifs ou
              non successifs.
            </p>
            <p>
              Les décisions seront reprises dans les P.V. qui seront consignés dans un registre spécial et contresignés
              par au moins deux administrateurs qui étaient présents à la réunion. Les copies ou extraits à produire en
              justice ou ailleurs seront signés par deux administrateurs.
            </p>
            <p>
              <b>Article 7 - DELEGATION DE POUVOIR</b>
            </p>
            <p>
              Chaque délégation de pouvoirs fait l'objet d'une décision du Conseil d'Administration. Le point devra être
              inscrit à l'ordre du jour et ne pourra être voté, dans le cas ou cette délégation possède un caractère
              permanent, que pour autant que 2/3 des administrateurs soient présents ou représentés.
            </p>
            <p>
              <b>Article 7.1 - Gestion</b>
            </p>
            <p>
              Le Conseil d'Administration peut désigner parmi les membres du personnel de la coopérative un directeur
              auquel sera confiée la gestion quotidienne de la coopérative dans les limites que le conseil décidera.
            </p>
            <p>
              <b>Article 7.2 - Tension salariale</b>
            </p>
            <p>
              La tension salariale entre la rémunération la plus élevée et la plus faible parmi les membres du personnel
              de la coopérative ne dépassera pas trois. Les conditions salariales sont établies de manière transparente
              et aussi objectivable que possible.
            </p>
            <p style="font-size:15px;">
              <b>Chapitre 4: ASSEMBLEE GENERALE</b>
            </p>
            <p>
              <b>Article 8. CONVOCATIONS</b>
            </p>
            <p>
              L'Assemblée Générale ordinaire est convoquée de la manière prévue par les statuts (ARTICLE 22).
            </p>
            <p>
              L'Assemblée Générale ordinaire se tient le premier vendredi du mois de mai, chaque année. Si ce jour
              tombe au milieu d’un week-end spécial, cette date pourra être reportée d’une semaine, à condition que
              les coopérateurs en aient été prévenus nonante (90) jours à l’avance.
            </p>
            <p>
              Les convocations sont envoyées au minimum 15 jours à l’avance. La convocation est envoyée par courriel,
              lorsque le coopérateur a marqué son accord pour cette forme de communication lors de sa souscription,
              ou par simple lettre dans le cas contraire. La convocation indiquera les dates, heures et lieux de l’AG et
              l’ordre du jour sur lequel l’Assemblée pourra valablement délibérer. Un modèle de procuration sera
              également joint. Lorsqu’une AG est concernée par une procédure d’appel à candidatures pour le
              renouvellement ou l’admission d’administrateurs, les candidatures sont jointes à la convocation.
              Tous les coopérateurs ayant souscrits et libérés leurs parts au moins 15 jours avant l’AG seront
              convoqués. Toutefois, tout coopérateur ayant souscrit et libéré ses parts entre ce moment et la veille de
              l’AG pourra y participer pour autant qu’il réponde aux critères d’admission décrits à l’Article 3 du présent
              ROI.
            </p>
            <p>
              Les convocations d’assemblées extraordinaires se feront suivant les mêmes modalités que les AG
              ordinaires au moins 15 jours à l’avance. La convocation, précisant l’ordre du jour, devra aussi être
              accompagnée de la (des) raison(s) qui justifie(nt) la tenue d’une telle AG.
            </p>
            <p>
              <b>Article 9 – ORDRE DU JOUR</b>
            </p>
            <ul>
            <li>
              Les statuts précisent que c’est le président qui constitue l’ordre du jour. Celui-ci soumet au conseil
              d’administration pour validation l’ordre du jour de l’AG.
            </li>
            <li>
              Les coopérateurs, garants ou ordinaires, peuvent également soumettre un point à l’ordre du jour de
              l’AG. Le point proposé, documenté (développé), argumenté (pertinence) et soutenu par deux
              administrateurs au moins est préalablement porté à l’agenda du CA qui délibère et décide de son
              introduction à l’AG. Il sera introduit à l’ordre du jour de l’AG sous la responsabilité des administrateurs qui
              ont souhaité soutenir ce point. La demande devra parvenir au président du CA, avec copie au secrétaire,
              au moins 30 jours avant la date de l’AG, par courrier ou courriel (avec accusé de réception)
            </li>
            </ul>
            </p>
            <p>
              <b>Article 10. LES PROCURATIONS</b>
            </p>
            <p>
              Les procurations à l’AG précisent la date de la réunion, le nom et le prénom du mandataire et du mandant
              et sont signées par ceux-ci. Le document est remis lors de l’inscription en début de réunion. Elles sont
              jointes à l’original du PV de l’AG.
            </p>
            <p>
              Un membre présent ne peut être porteur que de trois procurations au maximum.
            </p>
            <p>
              Un tuteur légal détient le droit de vote pour le(s) coopérateur(s) mineur(s) (au jour de l’AG) qu’il
              représente. Ce droit de vote n’est pas considéré comme une procuration.
            </p>
            <p>
              Seuls des coopérateurs de Lucéole peuvent être porteurs de procurations pour d’autres membres.
            </p>
            <p>
              Un coopérateur Garant ne peut donner procuration qu’à un autre coopérateur Garant.
            </p>
            <p>
              <b>Article 11 – VOTE EN ASSEMBLEE GENERALE</b>
            </p>
            <p>
              Pour être valablement adoptés, les points à l’Ordre du Jour de l’AG qui sont soumis au vote doivent
              obtenir la majorité absolue des voix présentes et représentées (50%+1) parmi les coopérateurs ordinaires
              et parmi les coopérateurs garants. Il n’est pas tenu compte des abstentions.
            </p>
            <p>
              Lors des votes portant sur des modifications de statuts, des majorités spéciales sont exigées par les
              statuts : majorité d’au moins 3/4 des voix présentes et représentées dans les deux collèges pour les
              modifications de statuts et de 4/5 des voix présentes et représentées dans les deux collèges s’il s’agit de
              modifications à l’Objet social.
            </p>
            <p>
              Lors de l’AG tous les votes ont lieu à bulletin secret.
            </p>
            <p>
              Le bureau de dépouillement est constitué par au moins un administrateur, un coopérateur garant et un
              coopérateur ordinaire. Les résultats des votes sont annoncés à l’AG par le président de séance et sont
              consignés dans le PV de l’AG et les bulletins conservés avec l’original du PV.
            </p>
            <p>
              <b>Article 12 – REMUNERATIONS DES ADMINISTRATEURS</b>
            </p>
            <p>
              Toutes les fonctions exercées au sein de la coopérative, telles que membres du Conseil d'Administration,
              administrateur délégué, gérant, membre du comité de crédit ou de secrétaire, scrutateurs, etc. le sont sur
              base des règles du volontariat tels que définies par la loi.
            </p>
            <p>
              Si, pour quelque raison, il fallait envisager la rétribution de l'une des fonctions ci-dessus, dans le cadre de
              l'article 16 des statuts, il appartiendrait au Conseil d'Administration d'en arrêter les modalités et
              conditions et de donner à cette fin, mandat au Conseil d'Administration d'en assurer l'exécution.
            </p>
            <p>
              Le remboursement des frais et débours sera de la compétence du Conseil d'Administration.
            </p>
            <p style="font-size:15px;">
              <b>Chapitre 5 – INTELLIGENCE COLLECTIVE / GROUPES DE COMPETENCES ET DE PROPOSITIONS</b>
            </p>
            <p>
              <b>Article 13 – GROUPES DE COMPETENCES ET DE PROPOSITIONS : GCP </b>
            </p>
            <p>
              <b>Article 13.1  Principes </b>: nature, fonction et position dans la gouvernance de Lucéole
            </p>
            <p>
              La coopérative Lucéole a pour objet « d’impliquer les citoyens dans le développement des énergies
              renouvelables, en particulier de l’éolien, de manière à assurer une gestion, une exploitation et un contrôle
              citoyens sur la production et la fourniture d’énergie. » Cette citoyenneté participative se traduit dans le
              développement de « l’intelligence collective  de Lucéole » à travers la mise en place de Groupes de
              Compétences et de Propositions. L’intelligence collective désigne les savoirs & pratiques d'une
              communauté résultant des interactions multiples entre ses membres (ex: fourmis/oiseaux migrateurs,…).
              Elle est la plus value que l’interaction donne à la somme des intelligences individuelles. Elle veille à
              donner une vraie place à l’expérience et aux savoir-faire des coopérateurs dans le rapport aux experts
              extérieurs.
            </p>
            <p>
              A cette fin Lucéole a créé des groupes de travail internes appelés Groupes de Compétences et de
              Propositions ou GCP. Ces groupes sont mixtes (administrateurs & coopérateurs) et ont une double
              mission:
            </p>
            <ol>
            <li>
              acquérir et transmettre des compétences aux membres du GCP, au Conseil
              d’Administration et aux coopérateurs intéressés de Lucéole.
            </li>
            <li>
              sur base de ces compétences, faire des propositions au CA.
            </li>
            </ol>
            <p>
              <b>Article 13.2 Composition</b> : qualité de membre, coordinateur, second lien et secrétaire
            </p>
            <p>
              Les groupes de compétences sont composés d’administrateurs et de coopérateurs. Pour être membre
              d’un GCP il faut être coopérateur de Lucéole. Tout coopérateur peut adresser sa candidature au
              coordinateur du GCP qui la proposera au GCP pour acceptation. Le GCP est relié au Conseil
              d’Administration par deux personnes : le coordinateur et le second lien.
            </p>
            <p>
              Le GCP est coordonné par un administrateur qui est nommé par le CA. Le coordinateur a pour mission
              d’animer le GCP et d’être responsable du GCP au sein du CA. Le second lien est le représentant de l’esprit
              du GCP. Il participe aux décisions du CA concernant le GCP. Le second lien est un médiateur naturel,
              quelqu’un à qui l’on fait tout naturellement confiance grâce à sa compétence et ses qualités. Le GCP se
              choisit de façon autonome le second lien et le secrétaire du GCP. Le secrétaire a la responsabilité de faire
              un compte rendu des décisions prises par le GCP et de les mettre à la disposition des membres du CA.
            </p>
            <p>
              <b>Article 13.3 Organisation</b> : feuille de route et compte rendu au CA
            </p>
            <p>
              Le cadre de travail des GCP est fixé par la feuille de route du GCP. Avant d’entamer ses activités, le GCP
              soumet une feuille de route au CA. Le cadre de la feuille de route est fixé par la définition des enjeux qui
              est définie conjointement avec le conseil d’administration. Sur base de ces enjeux, le GCP rédige une
              feuille de route qui comprend les rubriques suivantes : objectifs, définition des activités, planning,
              résultats attendus ainsi qu’un budget pour les moyens à mettre en oeuvre. La feuille de route ainsi
              rédigée est proposée au CA qui la valide. La feuille de route approuvée et les fonctions attribuées au sein
              du GCP celui-ci est libre de s’organiser comme il le souhaite fréquence et lieu des réunions, type compte
              rendu, etc.)
            </p>
            <p>
              Les relations entre le CA et le GCP sont régies par le principe du double lien : le coordinateur et le second
              lien. Le GCP rend compte au CA à travers le compte rendu conjoint du coordinateur et du second lien, ceci
              quand ils le jugent nécessaire ou sur demande du CA pour l’informer ou soumettre à décision une
              proposition. Le GCP donne accès au compte rendu des décisions prises lors de ses réunions aux
              administrateurs qui le souhaitent. Le GCP veille à proposer une nouvelle feuille de route ou une mise à
              jour de sa feuille de route au CA lorsque ses activités demandent une adaptation ou une redéfinition de
              la feuille de route.
            </p>
            <p>
              Le CA nomme parmi ses administrateurs un responsable « intelligence collective » qui est chargé du suivi
              des GCP et du respect des procédures.
            </p>
            <p>
              <b>Article 13.4 Place du GCP dans le processus de décision</b>
            </p>
            <p>
              Dans le processus de décision en trois séquences de Lucéole : 1. Proposition, 2. Décision, 3. Mise en
              œuvre, l’action des GCP se situe « en amont » de la décision sur la séquence « proposition » et de façon
              optionnelle sur la séquence trois « mise en œuvre ». Le CA garde, à la lumière des travaux et des
              compétences qu’a développées le GCP, la responsabilité de la prise de décision (séquence 2).
            </p>
            <p>
              <b>Article 13.5 Gestion de la confidentialité</b>
            </p>
            <p>
              Les membres du GCP sont tenus par un devoir de confidentialité vis-à-vis de l’extérieur. Cette
              confidentialité concerne les informations qu’ils échangent ainsi que le contenu des débats internes au
              groupe.
            </p>
            <p style="font-size:15px;">
              <b>Chapitre 6 – LES PARTS DES COOPERATEURS</b>
            </p>
            <p>
              <b>Article 14 – REMUNERATION DES PARTS</b>
            </p>
            <p>
              Conformément à l'article 33 des statuts, les parts de catégorie A, B et C pourront se voir attribuer un
              dividende. Ce dividende ne pourra dépasser le pourcentage prévu par la loi.
            </p>
            <p>
              <b>Article 15 - DISSOLUTION</b>
            </p>
            <p>
              En cas de cessation d’activités de la coopérative et décision de dissolution volontaire par l’AG, il sera
              procédé à la réalisation de l’actif et après paiement des dettes et charges de la société, lors de la clôture
              de la liquidation, l’actif net sera réparti entre les coopérateurs, au prorata des parts détenues par chacun,
              sans qu’il ne puisse toutefois être versé de plus-value sur les parts. Le solde éventuel, après
              remboursement des parts des coopérateurs, sera versé à une ou plusieurs structure(s) poursuivant des
              objectifs proches de ceux prônés par Lucéole.
            </p>
            <p>
              Le CA sera chargé de trouver cette structure et de s’assurer de la bonne gestion des fonds confiés.
            </p>

