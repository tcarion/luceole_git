                L'article 36 des statuts de la coopérative précise <i>«Un règlement d'ordre intérieur, fixant les responsabilités
                de tous ceux qui participent à la gestion, à l'animation, à la surveillance et au contrôle de l'activité de la
                coopérative, est établi et modifié par le conseil d'administration; toute modification du règlement d'ordre
