            <div class="pages-body">
              <p><b>TITRE I – DENOMINATION – SIEGE - OBJET SOCIAL</b></p>
              <p>
                <b>ARTICLE 1 : DENOMINATION</b><br>
                La société est une société coopérative à responsabilité limitée qui existe sous la
                dénomination « Lucéole SCRL », désignée ci-après « la coopérative ».
              </p>
              <p>
                <b>ARTICLE 2 : SIEGE SOCIAL - SIEGE D'EXPLOITATION</b> <br>
                Le siège social est établi à Rue Abraham Gilson 1 à 6723 HABAY-LA-VIEILLE.
                Il peut être transféré ailleurs en Belgique par décision du conseil d'administration.
                La société peut établir, sur décision du conseil d'administration, différents sièges
                d'exploitation.
            </p>

            <p>
              <b>ARTICLE 3 : OBJET SOCIAL </b> </br>
              La société coopérative Lucéole SCRL a pour objet de :
              <ul>
                <li>
                  Impliquer les citoyens dans le développement des énergies
                  renouvelables, en particulier de l’éolien, de manière à assurer une
                  gestion, une exploitation et un contrôle citoyens sur la production et la
                  fourniture d’énergie.
                </li>
                <li>
                  Réaliser des investissements durables dans le domaine des énergies
                  renouvelables.
                </li>
                <li>
                  Promouvoir auprès de ses associés et du grand public une utilisation
                  rationnelle et responsable de l’énergie et une orientation vers l’utilisation
                  d’énergies d’origine renouvelable.
                </li>
                <li>
                  Appuyer ses associés pour réaliser des choix énergétiques liés à la mise
                  en œuvre des solutions individuelles de production et de consommation
                  durables d’énergie.
                </li>
              </ul>

              La société n’est pas vouée principalement à l’enrichissement de ses associés,
              lesquels ne recherchent qu’un bénéfice patrimonial limité. La société cherche plutôt
              à favoriser la participation citoyenne dans des projets locaux, démocratiques et
              éducatifs. La société veut favoriser le développement économique local par la
              création d’emploi dans le développement durable.
              <br>
              Dans ses participations financières et ses activités propres, la société veille à
              générer de manière équilibrée des profits économiques pour ses membres et des
              bénéfices environnementaux et sociaux.<br>
              La société peut notamment avoir des activités dans la production, l'achat et la vente
              d'énergie renouvelable (éolienne, solaire, hydraulique, biomasse, …). La société
              cherche à ce que ses associés puissent consommer l’énergie renouvelable
              produite.<br>
              La société peut réaliser toutes opérations se rattachant directement ou
              indirectement à la réalisation de l'objet ainsi défini. Elle peut faire en général, toutes
              opérations industrielles, commerciales, agricoles, financières, mobilières et
              immobilières se rapportant directement ou indirectement, en tout ou en partie à son
              objet ou susceptible d'étendre ou de développer son activité dans le cadre de son
              objet.<br>
              La société peut s'intéresser par voie d'apport, de participation, de fusion, de
              souscription, de partenariat ou par tout autre mode dans toutes sociétés ou
              entreprises ayant un objet similaire ou connexe. La société doit procurer à ses
              associés un avantage économique ou social, dans la satisfaction de leurs besoins
              professionnels ou privés.<br>
              La société cherche à développer des synergies locales entre activités publiques,
              citoyennes et privées afin d’obtenir un impact significatif en terme de
              développement durable.<br>
              La société a choisi comme investissement prioritaire la participation à la réalisation
              d’un parc éolien à Habay.<br>
              La coopérative peut dans le sens le plus large, exercer toutes activités susceptibles
              de favoriser la réalisation de son objet social et participer à une telle activité de
              quelque manière que ce soit. Elle peut recevoir ou emprunter les fonds nécessaires
              à ses activités, sous réserve des dispositions légales et réglementaires relatives à
              la protection de l’épargne publique.
            </p>
            <p>
              <b>ARTICLE 4 : DUREE </b> <br>
              La coopérative est a durée illimitée. Elle peut être dissoute par décision de
              l’assemblée générale statuant dans les formes et conditions prévues pour les
              modifications des statuts.
            </p>
            <p>
              <b>TITRE II – CAPITAL - PARTS SOCIALES - RESPONSABILITES</b>
            </p>

            <p>
              <b>ARTICLE 5 : CAPITAL</b> <br>
              Le capital social est illimité. Le capital social doit être entièrement et
              inconditionnellement souscrit. Sa part fixe est fixée à dix-huit mille six cents euros
              (18.600 EUR).
            </p>
            <p>
              <b>ARTICLE 6 : PARTS SOCIALES </b> <br>
              Le capital social est représenté par des parts sociales de trois catégories:
              <ol>
              <li>
              Catégorie A : parts de coopérateurs garants, valeur nominale fixée à
              cent euros (100 EUR).
              </li>
              <li>
              Catégorie B : parts de coopérateurs ordinaires, personnes physiques,
              valeur nominale fixée à cent euros (100 EUR).
              </li>
              <li>
              Catégorie C : parts de coopérateurs ordinaires, personnes morales,
              valeur nominale fixée à cent euros (100 EUR).
              </li>
              </ol>
              La part fixe du capital devra toujours être souscrite par des membres garants ou
              ordinaires de catégorie B. <br>
              Les parts sociales doivent être entièrement libérées au moment de leur
              souscription. <br>
              Les dividendes accordés sur ces parts seront limités à six pour cent (6 %).
            </p>
            <p>
              <b>ARTICLE 7 : TRANSFERTS DE PARTS</b> <br>
              Les parts sociales ne peuvent être cédées ou transmises qu’à des coopérateurs et
              moyennant l’accord du conseil d’administration.
            </p>
            <p>
              <b>ARTICLE 8 : RESPONSABILITE</b> <br>
              La responsabilité des coopérateurs est limitée au montant de leur souscription.
            </p>
            <p>
              <b>TITRE III - COOPERATEURS</b>
            </p>
            <p>
              <b>ARTICLE 9 : ADMISSION</b> <br>
              Pour devenir et rester membre de la coopérative, il faut : <br>
              Etre admis par le conseil d’administration. <br>
              Avoir souscrit et libéré, conformément aux prescriptions énoncées par le conseil
              d’administration, une ou plusieurs parts sociales comme membre garant ou
              membre ordinaire. <br>
              Avoir pris connaissance du règlement d’ordre intérieur. <br>
              Sont coopérateurs:
              <ol>
              <li>
              Les signataires de l’acte constitutif du quinze octobre deux mil dix,
              fondateurs de la société; ces fondateurs ont également la qualité de “coopérateurs
              garants” de l’objet social de la société et la conservent tant qu’ils le souhaitent;
              </li>
              <li>
              Les coopérateurs garants auront la faculté de coopter par un vote à la
              majorité simple de leur collège, lors de l’assemblée générale, parmi les
              coopérateurs ordinaires, des candidats ayant des engagements forts vis à vis des
              présents statuts; le nombre total des coopérateurs garants ne peut cependant
              dépasser cent cinquante pour cent (150 %) du nombre initial des coopérateurs
              fondateurs
              </li>
              <li>
              D’autres sociétaires appelés coopérateurs ordinaires admis par le
              conseil d’administration et souscrivant à au moins une part aux conditions fixées
              par le conseil d’administration et précisées par le règlement d’ordre intérieur, étant
              entendu que cette souscription implique l’acceptation des statuts et du règlement
              d’ordre intérieur.<br>
              Un coopérateur peut souscrire des parts de différentes catégories. Les services de
              la coopérative sont réservés aux coopérateurs. La qualité de coopérateur est
              constatée sur un registre des coopérateurs qui indiquera la date d’admission et s’il
              s’agit d’un coopérateur garant ou d’un coopérateur ordinaire.
              </li>
            </ol>
            </p>
            <p>
              <b>ARTICLE 10 : DEMISSION</b> <br>
              Tout coopérateur ne peut démissionner que dans les six premiers mois de
              l’exercice social.
            </p>
            <p>
              <b>ARTICLE 11 : EXCLUSIONS</b> <br>
              Un coopérateur peut être exclu de la société s’il commet des actes contraires aux
              intérêts de la société. Les exclusions sont prononcées par le conseil
              d’administration.<br>
              L’associé dont l’exclusion est demandée doit être invité à faire connaître ses
              observations par écrit devant l’organe chargé de se prononcer, dans le mois de
              l’envoi d’un pli recommandé avec accusé de réception contenant la proposition
              motivée d’exclusion.<br>
              S’il le demande dans l’écrit contenant ses observations, l’associé doit être entendu.
              Toute décision d’exclusion est motivée.<br>
              Une copie conforme du procès-verbal de l’exclusion est notifiée par lettre
              recommandée dans les quinze jours à l’associé exclu.
            </p>
            <p>
              <b>ARTICLE 12 : REMBOURSEMENT</b> <br>
              Un remboursement est en principe subordonné à un délai de douze mois, à dater
              du jour de la démission, de la demande de remboursement partiel ou total ou de
              l’exclusion.<br>
              Toutefois, le conseil d’administration peut déroger à cette règle et anticiper ou
              postposer le remboursement en respectant les règles fixées au règlement d’ordre
              intérieur (R.O.I.) selon les catégories de parts.<br>
              Le conseil d’administration a la faculté de postposer un remboursement si celui-ci
              avait pour conséquence de mettre gravement en péril la trésorerie de la
              coopérative ou de réduire l’actif net suivant la définition de l’article 429 du Code des
              sociétés en dessous de la part fixe du capital social.<br>
              Le coopérateur démissionnaire ou exclu a droit au remboursement de sa part telle
              qu’elle résulte du bilan de l’année sociale pendant laquelle la démission a été
              donnée ou l’exclusion prononcée, sans toutefois qu’il soit attribué une partie des
              réserves ou des fonds spéciaux.<br>
              En aucun cas, il ne peut être remboursé plus que la partie libérée par le
              coopérateur sur sa part.<br>
              Sous réserve de ce que prévoient les alinéas précédents tout retrait de versement
              sur les parts est interdit.
            </p>
            <p>
              <b>ARTICLE 13</b> <br>
              En cas de décès, de faillite, de déconfiture ou d’interdiction d’un coopérateur, ses
              héritiers, créanciers ou représentants légaux recouvrent la valeur de ses parts
              conformément aux dispositions précédentes.
            </p>
            <p>
              <b>ARTICLE 14</b> <br>
              Les coopérateurs et les ayants droit ou ayants cause d’un coopérateur ne peuvent
              provoquer la liquidation de la société, l’apposition de scellés, la liquidation ou le
              partage de l’avoir social, ni intervenir de quelque manière que ce soit dans
              l’administration de la société.<br>
              Ils doivent, pour l’exercice de leurs droits, s’en rapporter aux livres et écritures
              sociaux et aux décisions du conseil d’administration et de l’assemblée générale. En
              cas de propriété indivise d’une part, la société a le droit de suspendre l’exercice des
              droits des héritiers ou des propriétaires indivis jusqu’à ce qu’une personne, agréée
              en qualité de coopérateur conformément à I article 9, soit désignée comme titulaire.
            </p>
            <p>
              <b>TITRE IV - ADMINISTRATION ET</b>
            </p>
            <p>
              <b>ARTICLE 15</b> <br>
              La coopérative est administrée par un conseil d’administration composé de sept (7)
              membres au moins et de neuf (9) membres au maximum, possédant la qualité de
              coopérateurs, nommés par l’assemblée générale à la majorité absolue (moitié plus
              une des voix présentes ou représentées) à la fois des coopérateurs garants
              (catégorie A) et des coopérateurs ordinaires (catégories B+C). La durée du mandat
              des administrateurs est de quatre ans ; ils sont rééligibles.
              La qualité d’administrateur est réservée à des personnes n’exerçant aucun mandat
              politique.<br>
              Ne peut être élu au conseil d'administration une personne exerçant un mandat dans
              une structure publique ou privée dont les activités peuvent générer un conflit
              d’intérêt avec les projets d’investissements de la coopérative.
              En cas de vacance d’un poste d’administrateur, le conseil d’administration peut
              pourvoir au remplacement jusqu’à ce que l’assemblée générale suivante en décide
              de manière définitive.
            </p>
            <p>
              <b>ARTICLE 16</b> <br>
              L’administrateur remplaçant un autre achève le mandat de celui-ci. Ils sont en tout
              temps révocables par l’assemblée générale.<br>
              Les mandats des administrateurs sont gratuits.<br>
              Toutefois en ce qui concerne les administrateurs chargés d’une délégation
              comportant des prestations spéciales ou permanentes, il peut être accordé une
              rémunération; en aucun cas, cette rémunération ne peut consister en une
              participation au bénéfice de la coopérative.
            </p>
            <p>
              <b>ARTICLE 17</b> <br>
              Sauf décision contraire de l’assemblée générale et tant que la société répond, pour
              le dernier exercice clôturé, aux critères énoncés à l’article 15 du code des sociétés,
              la société n’est pas tenue de nommer de commissaire.<br>
              Dans ce cas, chaque associé a individuellement les pouvoirs d’investigation et de
              contrôle du commissaire. Il peut se faire représenter par un expert comptable.
              Cependant, conformément à l’article 385 du code des sociétés, aussi longtemps
              que la société répond aux dits critères et qu’aucun commissaire n’est nommé,
              l’assemblée peut désigner un ou plusieurs associés auxquels elle délègue les
              pouvoirs d’investigation et de contrôle des associés individuels.<br>
              Ce ou ces associés ne peuvent exercer aucune autre fonction ni accepter aucune
              autre mission ou mandat dans la société. Ce ou ces associés peuvent se faire
              représenter par un expert-comptable dont la rémunération incombera à la société
              s’il a été désigné avec l’accord de celle-ci ou si cette rémunération a été mise à sa
              charge par décision judiciaire.<br>
              Au cas où la société ne répondrait plus aux critères indiqués ci-avant, l’assemblée
              générale doit se réunir dans le plus bref délai pour procéder à la nomination d’un ou
              de plusieurs commissaire(s) dans les conditions prévues par la loi.
            </p>
            <p>
              <b>ARTICLE 18</b> <br>
              Le conseil d’administration choisit parmi ses membres un président et peut choisir
              un secrétaire et un trésorier.<br>
              II se réunit sur convocation et sous la présidence de son président ou, en cas
              d’empêchement, de celle de l’administrateur le plus âgé, et chaque fois que deux
              administrateurs au moins le demandent.<br>
              Le conseil d’administration ne peut délibérer valablement que si la moitié au moins
              de ses membres sont présents ou représentés.<br>
              Un administrateur peut se faire représenter mais uniquement par un autre
              administrateur et chaque administrateur ne peut être porteur que d’une seule
              procuration.<br>
              Le règlement d’ordre intérieur peut préciser la forme requise pour la procuration :
              document original, document photocopié, document télécopié, document scanné,
              document transmis par courriel ou courrier électronique voire encore toutes autres
              formes admises par le conseil d’administration à la réunion concernée.<br>
              Les décisions sont prises à la majorité absolue (la moitié des voix plus une) des
              membres présents et représentés. Il n’est pas tenu compte des abstentions.
            </p>
            <p>
              <b>ARTICLE 19</b> <br>
              Le conseil d’administration est investi des pouvoirs les plus étendus pour faire tous
              les actes de gestion et de disposition entrant dans le cadre de l’objet social, à
              l’exception de ceux que la loi ou les statuts réservent à l’assemblée générale.
            </p>
            <p>
              <b>ARTICLE 20</b> <br>
              Le conseil d’administration peut déléguer des pouvoirs à un ou plusieurs de ses
              membres ou à des tiers.<br>
              Il peut aussi et notamment :<br>
              Confier la gestion journalière de la société à un administrateur délégué ou à un
              gérant ou à un directeur, en ce compris les actions en justice;
              Constituer un comité de gestion dont les membres sont choisis parmi les
              administrateurs ou non et en organiser le fonctionnement.<br>
              La société est valablement représentée vis-à-vis des tiers, y compris dans les actes
              requérant la présence d’un officier ministériel par :
              <ul>
              <li>
              Soit, deux administrateurs agissant conjointement;
              </li>
              <li>
              Soit, le président.
              </li>
              </ul>
              Le conseil d’administration détermine la rémunération attachée aux délégations
              qu’il confère en tenant compte des dispositions de l’article 16.
            </p>
            <p>
              <b>TITRE V - ASSEMBLÉE GÉNÉRALE</b>
            </p>
            <p>
              <b>ARTICLE 21</b> <br>
              Tout membre ayant souscrit et libéré conformément aux décisions du conseil
              d’administration au moins une part de coopérateur garant et/ou de coopérateur
              ordinaire fait partie de droit de l’assemblée générale. Celle-ci possède les pouvoirs
              qui lui sont attribués par la loi.
            </p>
            <p>
              <b>ARTICLE 22</b> <br>
              L’assemblée générale se réunit au moins une fois par an, le premier vendredi de
              mai à vingt heures, au siège social de la société ou à un autre endroit précisé par la
              convocation.<br>
              Si ce jour tombe au milieu d’un week-end spécial (par exemple après le jeudi de
              l’Ascension ou avant le week-end de Pentecôte), cette date peut être reportée
              d’une semaine, à condition que les coopérateurs en aient été prévenus nonante
              (90) jours à l’avance.<br>
              La convocation doit se faire quinze (15) jours au moins avant la réunion, par lettre
              ordinaire ou moyennant l’accord préalable du coopérateur par courriel (courrier
              électronique) avec notification de la réception.<br>
              Elle doit en tout état de cause mentionner la date, l’heure, le lieu et les points à
              l’ordre du jour.
            </p>
            <p>
              <b>ARTICLE 23</b> <br>
              L’assemblée générale est présidée par le président du conseil d’administration ou,
              à défaut, par l’administrateur le plus âgé ou le plus ancien en fonction.
            </p>
            <p>
              <b>ARTICLE 24</b> <br>
              Chaque coopérateur a droit à une voix quel que soit le nombre de ses parts.
              Chaque coopérateur peut en représenter plusieurs autres avec une limite de trois
              (3). Dans ce cas il doit être en possession d’une procuration dûment signée par
              chacun des coopérateurs qu’il représente.<br>
              L’assemblée ne peut délibérer que sur les points repris à l’ordre du jour.
              Les décisions doivent être approuvées à la majorité absolue des voix présentes et
              représentées des coopérateurs garants (catégorie A) et à la majorité absolue des
              voix présentes et représentées des coopérateurs ordinaires (catégories B + C). Il
              n’est pas tenu compte des abstentions.
            </p>
            <p>
              <b>ARTICLE 25</b> <br>
              L’assemblée générale ne peut valablement délibérer sur des modifications aux
              statuts que si l’objet des modifications a été spécialement indiqué dans la
              convocation et si ceux qui assistent à la réunion représentent au moins la moitié
              des voix y compris celles des procurations.<br>
              Si cette dernière condition n’est pas remplie, une nouvelle convocation sera
              nécessaire, et la nouvelle assemblée délibérera valablement, quel que soit le
              nombre de parts représentées.<br>
              Dans l’un et l’autre cas, aucune modification n’est admise que si elle réunit les trois
              quarts (¾) des voix présentes ou représentées des coopérateurs garants (catégorie
              A) ainsi que les trois quarts (¾) des voix présentes ou représentées des
              coopérateurs ordinaires (catégories B + C).
            </p>
            <p>
              <b>ARTICLE 26</b> <br>
              Lorsque la modification des statuts porte sur l’objet social de la société, une
              justification détaillée de la modification proposée est exposée par le conseil
              d’administration dans un rapport annoncé dans l’ordre du jour.<br>
              A ce rapport est joint un état résumant la situation active et passive de la société
              arrêté à une date ne remontant pas à plus de trois mois. Le commissaire fait un
              rapport distinct sur cet état.<br>
              Tout associé a le droit d’obtenir une copie de ces documents quinze jours au moins
              avant l’assemblée, sans frais et sur simple demande au président.<br>
              L’assemblée ne peut valablement délibérer que si ceux qui assistent à la réunion
              représentent au moins la moitié des voix présentes ou représentées.<br>
              Si cette dernière condition n’est pas remplie, une nouvelle convocation sera
              nécessaire et la nouvelle assemblée délibérera valablement quel que soit le
              nombre de parts présentes ou représentées.<br>
              Dans l’un et l’autre cas, aucune modification n’est admise que si elle réunit les
              quatre cinquième (4/5) des voix présentes ou représentées dés coopérateurs
              garants (catégorie A) ainsi que les quatre cinquième (4/5) des voix présentes ou
              représentées des coopérateurs ordinaires (catégories B + C).
            </p>
            <p>
              <b>ARTICLE 27</b> <br>
              Les procès verbaux de l’assemblée générale sont signés par le président et les
              coopérateurs qui le demandent par écrit. Les copies ou extraits à produire en
              justice ou ailleurs sont signés par le président ou l’administrateur gérant ou par
              deux administrateurs.
            </p>
            <p>
              <b>ARTICLE 28</b> <br>
              D’autres assemblées générales qu’ordinaires peuvent être convoquées par le
              conseil d’administration.<br>
              II doit en convoquer une chaque fois que le commissaire ou au moins vingt pour
              cent (20 %) du nombre de coopérateurs en font la demande, à condition de
              préciser ce dont ils veulent voir traiter à cette assemblée. L’assemblée doit être
              convoquée dans le mois de la demande.
            </p>
            <p>
              <b>ARTICLE 29</b> <br>
              Tout ce qui concerne l’activité du conseil d’administration, du commissaire et de
              l’assemblée générale peut être repris dans le règlement d’ordre intérieur sans que
              celui-ci puisse déroger aux stipulations impératives des statuts ou de la loi.
            </p>
            <p>
              <b>TITRE VI - EXERCICE SOCIAL - BILAN</b>
            </p>
            <p>
              <b>ARTICLE 30</b> <br>
              L’exercice social court du premier janvier au trente et un décembre de chaque
              année.
            </p>
            <p>
              <b>ARTICLE 31</b> <br>
              A la fin de chaque exercice social, le conseil d’administration dresse l’inventaire
              ainsi que le bilan et le compte de résultats et annexes à soumettre à l’assemblée
              générale.
            </p>
            <p>
              <b>ARTICLE 32</b> <br>
              L’assemblée générale annuelle entend les rapports des administrateurs et du
              commissaire et statue sur l’adoption des comptes annuels (bilan, compte de
              résultats et annexes). Après adoption des comptes annuels, l’assemblée générale
              se prononce sur la décharge des administrateurs et du commissaire. Les comptes
              annuels sont déposés dans les trente jours après leur approbation à la “Banque
              Nationale de Belgique”.
            </p>
            <p>
              <b>TITRE VII - REPARTITION BÉNÉFICAIRE</b>
            </p>
            <p>
              <b>ARTICLE 33</b> <br>
              Le bénéfice distribuable, tel qu'il résultera du bilan et en vertu de l'application de
              l'article 429 du Code des sociétés, sera affecté comme suit:
              <ul>
              <li>
              cinq (5%) à la réserve légale selon la prescription de la loi (ce
              prélèvement cesse d'être obligatoire lorsque le fonds de réserve légal a atteint le
              dixième de la part fixe du capital social)
              </li>
              <li>
              il peut être appliqué un dividende au montant effectivement libéré des
              parts de catégorie A-B et C; le taux de ce dividende sera décidé par l'assemblée
              générale sur proposition du conseil d'administration et ne pourra en aucun cas
              excéder celui fixé conformément à l'arrêté royal du huit janvier mil neuf cent
              soixante-deux fixant les conditions d'agréation de groupements nationaux de
              sociétés coopératives et de sociétés coopératives pour le Conseil National de la
              Coopération.
              </li>
              </ul>
              La ristourne qui serait éventuellement accordée aux coopérateurs ne peut être
              attribuée qu'au prorata des opérations qu'ils ont traitées avec la société.
            </p>
            <p>
              <b>TITRE VIII – DISSOLUTION - LIQUIDATION</b>
            </p>
            <p>
              <b>ARTICLE 34</b> <br>
              La coopérative est dissoute de plein droit si aucune régularisation n'intervient dans
              les six mois, notamment par la réduction du nombre de coopérateurs en dessous
              du minimum légal et par la réduction du capital en dessous de la part fixe de celui-
              ci.<br>
              Elle peut être dissoute par décision de l'assemblée générale statuant comme en
              matière de réforme des statuts; cette assemblée générale désigne un ou plusieurs
              liquidateurs.<br>
              Elle détermine leurs pouvoirs; le mode de liquidation et leurs émoluments
              éventuels.<br>
              Aussi longtemps que les liquidateurs n'auront pas été désignés, le conseil
              d'administration est de plein droit chargé de la liquidation.
            </p>
            <p>
              <b>ARTICLE 35</b> <br>
              Après paiement des dettes et charges sociales, le solde servira d'abord au
              remboursement des sommes versées en libération des parts. Ce remboursement
              se fera par catégories: en premier lieu seront remboursées solidairement les parts
              de catégorie B et C, puis les parts de catégorie A.<br>
              La répartition du solde restant, ou surplus de liquidation, sera décidée par
              l'assemblée générale qui devra l'affecter à un ou des organismes poursuivant un
              objet social similaire à celui de la société ou s'en rapprochant le plus possible.
            </p>
            <p>
              <b>TITRE IX - DISPOSITIONS DIVERSES</b>
            </p>
            <p>
              <b>ARTICLE 36</b> <br>
              Un règlement d'ordre intérieur, fixant les responsabilités de tous ceux qui
              participent à la gestion, à l'animation, à la surveillance et au contrôle de l'activité de
              la coopérative, est établi et modifié par le conseil d'administration; toute
              modification du règlement d'ordre intérieur doit être approuvée au plus tard par
              l'assemblée générale ordinaire qui suit.<br>
              Le règlement d'ordre intérieur peut, à condition de ne pas contrevenir aux
              dispositions impératives des statuts et de la loi, prendre toutes dispositions relatives
              à l'application des statuts et le règlement des affaires sociales en général, et peut
              imposer aux sociétaires et à leurs ayant droits pour ce qui est jugé utile aux intérêts
              de la coopérative.<br>
              Ce règlement peut être modifié par le conseil à condition que ce point soit inscrit à
              l'ordre du jour pour autant que les deux tiers des administrateurs au moins soient
              présents ou représentés.
            </p>
            <p>
              <b>ARTICLE 37</b> <br>
              Pour tout ce qui n’est pas prévu dans les présents statuts et, le cas échéant, par le
              règlement d’ordre intérieur, il sera référé au code des sociétés et aux autres
              dispositions légales ou usages régissant la matière concernée.<br>
              Enfin, toute disposition des présents statuts qui serait contraire à une disposition
              impérative est à considérer comme non écrite.
            </p>

