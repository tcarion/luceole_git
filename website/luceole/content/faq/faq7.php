        L’investissement dans toute entreprise comporte toujours une part de risques.
      </br>
        Le risque est plus important au cours de la première phase de chaque projet puisque l’obtention du permis n’est pas garantie alors que des frais doivent souvent être engagés (études d’incidences, procédure d’obtention de permis, de recours,…). </br>
        Le risque diminue lorsque le projet est réalisé et que l’activité de production est en route.</br>
        Le contrat d’extension de garantie qui est conclu avec le fabriquant (éolienne) nous met à l’abri des conséquences négatives des éventuelles pannes techniques.</br>
        La rentabilité d’un projet dépend essentiellement de la production, du prix de vente de l’électricité produite ainsi que des certificats verts garantis par la Région Wallonne.
