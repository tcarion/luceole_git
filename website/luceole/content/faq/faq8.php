      <p>
        L’argent des parts non investi est conservé sur un livret d’épargne (banque TRIODOS).</br>
        Il est investi exclusivement dans le développement des projets (études d’incidences, procédure d’obtention de permis, de recours,…) puis dans la réalisation des projets une fois les permis d’exploitation obtenus (achat éolienne par exemple), en complément d’emprunts bancaires.</br>
        Chaque projet (Habay, Fauvillers, Arlon,…) est / sera géré au sein de sociétés d’exploitation distinctes qui restituent à la coopérative leurs bénéfices d’exploitation, permettant notamment de verser des dividendes aux coopératrices/eurs.</br>
        Il n’est donc pas possible pour les coopératrices/eurs d’investir uniquement dans un projet précis.
      </p>
