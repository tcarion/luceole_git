        Pour rappel, le plafond de dividende annuel d’une coopérative agréée est limité à 6%, soit 300 € pour une mise maximale de 5.000 €. </br>
        Dès 2018 (déclaration fiscale 2019), les premiers 640 € de dividendes des parts de coopératives (éventuellement cumulés avec les dividendes d’autres types d’actions) seront exonérés du précompte mobilier de 30%.</br>
        Les sociétés coopératives devront retenir à la source 30% de précompte immobilier sur les dividendes versés. Le coopérateur devra indiquer les dividendes dans sa déclaration fiscale pour récupérer le précompte.
