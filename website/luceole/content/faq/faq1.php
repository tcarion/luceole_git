      <p>Une vraie coopérative citoyenne respecte les principes de l’Alliance coopérative internationale. </p>
      <p>Les coopératives sont des entreprises centrées sur les personnes, elles appartiennent à leurs membres et sont contrôlées par eux-mêmes pour réaliser leurs besoins et aspirations économiques, sociaux et culturels.
      Elles sont gérées selon la règle « un membre, une voix ». Les membres disposent ainsi du même droit de vote indépendamment du montant du capital qu'ils ont investi dans l'entreprise.
      </p>
      <p>
      Les coopératives aident les gens à prendre le contrôle de leur avenir économique et, du fait qu'elles n'appartiennent pas aux actionnaires, les avantages économiques et sociaux engendrés par leurs activités restent dans les communautés où elles sont établies. Les bénéfices générés sont soit réinvestis dans l'entreprise, soit reversés aux membres.
      Ces caractéristiques distinguent les vraies coopératives citoyennes des « coopératives industrielles » téléguidées par des multinationales ou de grandes sociétés privées. Les statuts de toutes les coopératives ne sont pas identiques. Il est toujours bon de les lire, surtout les chapitres concernant la gouvernance (le fonctionnement du Conseil d’Administration et de l’Assemblée Générale).
      </p>
