      <p>
        Le cycle de vie et le bilan énergétique des éoliennes ont fait l’objet d’analyses rigoureuses. La dépense énergétique de chaque étape a été calculée : depuis la fabrication de l’acier, jusqu’à la maintenance et le démantèlement, en passant par la construction, l’assemblage et le transport vers le site éolien. Cette dépense énergétique a ensuite été mise en comparaison avec la production d’énergie estimée de l’éolienne pendant toute sa durée de vie (jusqu’à 20 ans), en tenant compte du gisement venteux local. <b>Les études montrent ainsi que les éoliennes remboursent leur dette énergétique en moins d’un an. Elles produisent ensuite une énergie 100 % propre pendant le restant de leur cycle de vie.</b></br>
Une étude danoise montre par exemple que les éoliennes de 3 MW de type Vestas accumulent une dette énergétique de 4304 MWh sur l’ensemble de leur durée de vie. Avec un gisement venteux comme en Wallonie, une telle éolienne produira ces 4304 MWh en un peu moins de 8 mois.

