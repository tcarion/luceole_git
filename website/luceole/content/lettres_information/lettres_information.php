                de la demande de permis, pas simple...</b>
              </p>
              <p>
              Comme vous le savez, notre demande de permis pour le parc de 4
              éoliennes à Habay a été refusée par l’Administration wallonne le 12 février dernier. Entretemps, la déception et l’incompréhension ont fait place à l’action et les quatre partenaires de développement (Engie Electrabel, Ecopex, Elicio et lucéole) ont rapidement décidé d’introduire un recours contre cette décision auprès du ministre Di Antonio.
              </p>
              <p>
                <b>Complément d'étude d'incidence sur l'environnement (EIE)</b>
              </p>
              <p>
                Pour appuyer ce recours et garantir une meilleure sécurité juridique
                future au dossier, un complément d’EIE a été demandé au bureau SERTIUS.
                Il permet de justifier l’écart que le projet présente vis à vis du
                Schéma de Développement Communal (SDC) entré en vigueur fin 2017,
                dont la non prise en compte était à la base de la motivation du refus du 12 février.
                Ce complément d’EIE conclut que les dispositions du SDC de priorité
                agricole et d’intérêt paysager, ne sont pas incompatibles avec la
                construction et l’exploitation du parc éolien.
              </p>
              <p>
                <b>Nouvelle enquête publique</b>
              </p>
              <p>
                Pour que ce complément d’étude d’incidence puisse être valablement
                ajouté au dossier, il doit faire l’objet d’une enquête publique.
                Voilà pourquoi, depuis le 20 mars un nouvel affichage a été organisé,
                sur site et aux valves des communes de Habay, Etalle et Léglise,
                annonçant les dates d’ouverture et clôture et l’objet de ce complément d’enquête publique.
              </p>
